package less5s2;

public class Sorts {
    private int size;

    public Sorts(int size) {
        this.size = size;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    @Override
    public String toString() {
        return "Sorts{" +
                "size=" + size +
                '}';
    }
}
